package sept.ex_13092024;

public class Lab026_constants {
    public static void main(String[] args) {
        // Constants
        // Constants are nothing but the variables
        // whose value can't change once they are assigned
        final float pie = 3.1456f;
        // pie = 2.24f; compiler will throw the error
        System.out.println(pie);
    }
}
