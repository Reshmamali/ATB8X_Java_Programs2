package sept.ex_16092024;

public class Lab040_relational_operators {
    public static void main(String[] args) {
        System.out.println(10>10);
        System.out.println(10<10);
        System.out.println(10>=10);
        System.out.println(10<=10);
        System.out.println(10==10);
        System.out.println(10!=10);

    }
}
