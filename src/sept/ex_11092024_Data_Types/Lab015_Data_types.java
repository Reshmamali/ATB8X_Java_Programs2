package sept.ex_11092024_Data_Types;

public class Lab015_Data_types {
    public static void main(String[] args) {
//        Primitive Data Types
        byte b = 100; // 8 bits 1 Byte
        short i = 128; // 16 bits 2 Byte
        int s = 1000; // 32 bits 4 Byte
        long phone_number = 9589308455L; // 64 bits 8 Byte
        long phone_number2 = 9273506507L; // 64 bits 8 Byte

        char grade = 'A'; // ASCII value - int - assigned // 16 bits 2 Byte

        float f = 3.14f;
        float f2 = 3.14F; // 32 bits 4 Byte

        double d = 113.98786757; // 64 bits 8 Byte

//        Non primitive Data Types
        String s1 = "reshma"; // string is nothing but a bunch/collection of characters
        System.out.println(s1);
//        Array, Class >> User defined data types

    }
}
